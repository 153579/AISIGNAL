제 6조 (회원가입 및 이용계약의 성립)
① 회원가입은 ‘이용자’가 약관의 내용에 대하여 동의를 하고 회원가입신청을 한 후 ‘회사’가 이러한 신청에 대하여 승낙함으로써 회원가입이 성립됩니다. 
② ‘유료회원’은 반드시 본인의 실명으로 가입해야 합니다. 실명으로 가입하지 않은 ‘회원’은 법적인 보호를 받을 수 없으며, ‘서비스’ 이용제한을 받게 되며 관련법에     따라 처벌을 받을 수 있습니다. 
③ 회원가입계약의 성립 시기는 ‘회사’의 승낙이 ‘이용자’에게 도달한 시점으로 합니다. 
④ 회원으로 가입하여 서비스를 이용하기를 희망하는 자는 회사가 요청하는 소정의 신규회원가입 양식에서 요구하는 사항을 기록하여 신청합니다.
⑤ 온라인 가입신청 양식에 기재하는 사항에 실명이나 실제 정보를 입력하지 않은 자 는 법적인 보호를 받을 수 없으며, 서비스 사용의 제한을 받을 수 있습니다.
⑥ 회사는 이용자의 회원 가입신청 시 주민등록번호를 제외한 다음 각 호의 어느 하나의 방법에 따라 본인 여부를 확인합니다.
