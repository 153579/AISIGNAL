뉴비코 주식회사 이용약관

제1장  총칙 

제 1조 (목적) 
이 약관은 “뉴비코 주식회사” (이하 ‘회사’라 합니다)로 부터 유가증권 투자 등에 대한 정보를 제공받고자 하는 자(이하 “회원”라고 합니다)에게 회사가 종목 추천 등의 서비스(이하 “서비스”라고 합니다)를 제공함에 있어 회사와 회원 간의 권리, 의무 및 책임사항을 규정하는 것을 그 목적으로 합니다. 

제 2조 (정의)
이 약관에서 사용하는 용어의 정의는 다음과 같습니다. 
① “이용계약”이라 함은 서비스 이용을 위해 회사와 이용자 사이에 체결하는 계약을 말합니다. 
② "사이트"라 함은 회원들이 이용할 수 있는 회사가 운영하는 홈페이지
http://에이아이시그널.com/를 말합니다.
③ “이용요금”이라 함은 이용계약에 따라 회원이 회사에게 지급하여야 할 금원을 말합니다. 
④ ‘유료회원’이라 함은 ‘회사’와 사이트 이용계약을 체결하고, 이용요금을 납부함으로써 ‘회원’ 아이디를 부여 받은 자로서 ‘회사’의 정보를 지속적으로 제공받으며 ‘회사’가 제공하는 ‘서비스’를 지속적으로 이용할 수 있는 자를 말합니다.
⑤ ‘무료회원’이라 함은 ‘유료회원’이 아니면서 ‘회사’가 제공하는 ‘서비스’를 이용하는자를 말합니다. 
⑥ ‘서비스’라 함은 회원이 주식을 매도?매수하는데 편의성을 제공하기 위하여 회사가 정보통신 설비를 이용하여 주식정보 알고리즘을 회원의 증권 계좌 API와 연동시킨 컴퓨터 프로그램을 말합니다. 이중 유료회원이 이용하는 서비스는 ‘유료서비스’. 무료회원이 이용하는 서비스 ‘무료서비스’라 합니다. 
⑦ ‘아이디’라 함은 ‘회원’의 식별과 서비스 이용을 위하여 ‘회원’이 정하고 ‘회사’가 승인한 숫자 또는 문자+숫자의 조합을 말합니다. 
⑧ ‘비밀번호’라 함은 ‘회원’이 부여받은 ‘아이디’와 일치되는 ‘회원’임을 확인하고 비밀보호를 위해 ‘회원’ 자신이 정한 문자 또는 숫자의 조합을 말합니다.  

제 3조 (약관의 게시 등) 
① ‘회사’는 이 약관을 ‘회원’이 그 전부를 인쇄할 수 있고 거래과정에서 해당 약관의 내용을 확인할 수 있도록 기술적 조치를 취합니다. 
② ‘회사’는 ‘회원’이 약관에 동의하기에 앞서 약관에 정하여져 있는 내용 중 청약철회, 환불조건 등과 같은 중요한 내용을 이용자가 쉽게 확인할 수 있도록 기술적 조치를 취합니다. 

제 4조 (약관의 개정 등) 
① ‘회사’는 약관의 규제에 대한 법률 등 관련법을 위배하지 않는 범위에서 이 약관을 개정할 수 있습니다.
② ‘회사’가 약관을 개정할 경우에는 적용일자 및 개정사유를 명시하여 현행 약관과 함께 서비스 화면에 그 적용일자 7일 이전부터 적용일 후 상당한 기간 동안 공지합니다. 
③ ‘회사’가 약관을 개정할 경우에는 개정약관 공지 후 개정약관 적용에 대한 ‘유료회원’의 동의 여부를 확인합니다. 단, ‘유료회원’이 약관 개정 사실을 인지한 상태에서 동의 거부 의사를 별도 밝히지 않는 경우, 해당 약관에 동의한 것으로 간주 할 수 있습니다. 

제 5조 (약관의 해석 및 효력과 변경)
이 약관에서 정하지 아니한 사항과 이 약관의 해석에 관하여는 자본시장법, 전재상거래 등에서의 소비자보호에 관한 법률, 약관 규제에 관한 법률, 정보통신부장관이 정하는 디지털콘텐츠이용자보호지침, 기타 관계법령 또는 상 관례에 따릅니다. 

① 본 약관은 회사와 거래를 하는 모든 회원에게 그 효력을 발휘합니다. 
② 본 약관은 회사가 약관 내용을 회사의 웹사이트 게시하거나 기타의 방법으로 공지 한 후 이에 동의한 사람들이 회사와 회원가입계약을 체결함으로써 그 효력이 발생합니다. 
③ 회사는 회원의 동의를 확인하기 위하여 전자서명, 통화녹음 또는 자필서명 등의 방법으로 동의사실을 수집할 수 있습니다.
④ 회사는 관계법령을 위배하지 않는 범위에서 본 약관을 개정할 수 있으며 약관을 개정한 경우에는 적용일자 및 개정사유를 명시하여, 개정 전 약관과 함께 적용일 15영업일 전부터 회사 웹 사이트에서 확인할 수 있도록 게시하며, 기존 회원에게는 회원이 공개한 전자우편 주소로 이메일 발송, 홈페이지게시 등의 방법으로 약관 개정사실을 통지할 수 있습니다. 
⑤ 제4항과 같이 약관의 변경에 대하여 통지를 받은 회원이 통지를 받은 날로부터 15영업일 이내에 거절의 의사를 표시하지 않으면 변경약관에 동의한 것으로 간주합니다. 
⑥ 회원이 개정약관의 적용에 동의하지 않는 경우 회사는 회원이 이용 중인 유료 ‘서비스’의 이용이 종료될 때까지 기존 약관을 적용하거나, 이용계약을 해지 할 수 있습니다. 
⑦ 제6항에 따라 이용게약이 해지된 경우 회사는 회원에게 제16조의 환불기준에 따라 회원에게 환불하여 드립니다. 

