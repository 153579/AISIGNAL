5) 기타 회사가 정한 회원가입 신청 요건이 미비된 경우
③ 회사는 다음 각 호에 해당하는 경우에는 그 사유가 해소될 때까지 이용 승낙올 보류할 수 있으며, 그 사유를 웹사이트에 공지합니다.
1) 서비스 관련 설비의 용량이 부족한 경우
2) 기술상 또는 업무 수행상 지장이 있는 경우
3) 기타 회사가 필요하다고 인정하는 경우

